with sync_playwright() as p:
    browser = p.chromium.launch(channel="chrome", headless=True)
    page = browser.new_page(viewport={"width": 1400, "height": 900})
    errors = []
    page.on("pageerror", lambda exc: errors.append(str(exc)))
    page.on("dialog", lambda d: d.accept())

    # --- Fresh page, nothing purchased: plain real number, red, no note. ---
    page.goto(BASE)
    page.wait_for_selector("#treeWrap .node")
    sv0 = page.locator("#spentValue")
    print("fresh spentValue text/class:", sv0.inner_text(), sv0.get_attribute("class"))
    assert sv0.inner_text() == "0"
    assert sv0.get_attribute("class") is None
    color0 = sv0.evaluate("el => getComputedStyle(el).color")
    assert color0 == "rgb(217, 76, 76)", f"FAIL: expected real 'spent' red, got {color0}"
    assert sv0.get_attribute("title") is None
    print("PASS: no guesses purchased -> plain real number, red, no tooltip")

    # --- Load a build with several purchased-but-unconfirmed ranks. This
    # build code was generated by an older app version and still embeds a
    # "t" (totalPoints) field the app no longer reads at all - loading it
    # without error IS the backward-compatibility check. ---
    errors_before_load = len(errors)
    page.goto(f"{BASE}?build={BUILD}")
    page.wait_for_selector("#treeWrap .node")
    page.wait_for_timeout(200)
    assert len(errors) == errors_before_load, f"FAIL: loading an old build with a stale 't' field threw: {errors}"
    print("PASS: an old build code carrying a stripped-out 't' (totalPoints) field loads without error")

    sv = page.locator("#spentValue")
    print("spentValue text:", sv.inner_text())
    assert sv.inner_text() == "~194"
    assert "is-estimate" in sv.get_attribute("class")
    color = sv.evaluate("el => getComputedStyle(el).color")
    print("spentValue computed color:", color)
    assert color == "rgb(90, 169, 230)", f"FAIL: blended headline should render blue, got {color}"

    title = sv.get_attribute("title")
    print("spentValue title:", title)
    assert title == "171 confirmed + 23 estimated.", f"FAIL: unexpected tooltip text: {title}"
    print("PASS: headline blends to ~194 in blue, full breakdown lives only in the tooltip")

    # --- Progression's own running total now blends the same way the
    # topbar does - the last row's total must match the headline exactly
    # (~194), with the same "171 confirmed + 23 estimated." breakdown in
    # its own tooltip, proving the two displays agree rather than showing
    # two different numbers for the same underlying build. ---
    page.click('button[data-tab="progression"]')
    page.wait_for_timeout(100)
    prog_total_el = page.locator(".progression-row .cost-total").last
    prog_total = prog_total_el.inner_text()
    prog_title = prog_total_el.get_attribute("title")
    print("Progression's blended running total (must match the topbar's ~194):", prog_total, "|", prog_title)
    assert prog_total == "~194 total", f"FAIL: expected Progression's total to blend to ~194 like the topbar, got {prog_total}"
    assert "is-estimate" in prog_total_el.get_attribute("class")
    assert prog_title == "171 confirmed + 23 estimated.", f"FAIL: unexpected breakdown tooltip: {prog_title}"
    print("PASS: Progression's running total blends in estimates exactly like the topbar headline does, agreeing on both the figure and its breakdown")

    # --- Second, independent build: this is the exact scenario the bug was
    # originally reported against - Packrat trained to rank 10, its last 6
    # ranks (5-10) all carrying the same flat "~1" manual guess, mixed in
    # with plenty of real, fully-confirmed ranks earlier in the click
    # order. Real total 193, guesses add 6 (just Packrat's own 6 guessed
    # ranks now - Combat Stability rank 3 used to also be a guess here too,
    # but a wiki scrape confirmed it as a real 6, exactly matching what its
