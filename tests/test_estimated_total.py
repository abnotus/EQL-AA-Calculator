# The topbar's "Points Spent" headline number blends real + estimated costs
# (colored blue, ~194 instead of 171) when the build includes at least one
# purchased-but-unconfirmed rank with a guess - the ~ prefix and color are
# the only visible cue; the confirmed/estimated breakdown lives in the
# title tooltip only, same hover-to-disclose pattern as every other
# estimate badge in the app (no separate visible note element anymore -
# topbar is glanceable-density territory). Progression's own running total
# blends the exact same way now (it used to stay strictly real while the
# topbar blended, but a cumulative frozen through a step whose own pill
# shows a nonzero ~N estimate read as "the estimate isn't doing anything" -
# see logic.js's computeProgressionSteps for blendedCumulative). What's
# still guaranteed: spentPoints()/affordability math itself never reads a
# guess - "?" still costs exactly 0 there - proven here by the real/
# estimated split staying separately trackable (the "171 confirmed" portion
# of the tooltip) rather than the two numbers ever being silently merged
# into one indistinguishable figure. This is a real shared build with
# several purchased-but-unconfirmed ranks (First Aid, Innate Eminence,
# Improved Mend), independently verified by hand: real total 171, guesses
# add 23, so the headline (and now Progression's own last-row total) should
# read "~194". (Real total climbed from 164 to 171, and estimated dropped
# from 32 to 23, across two separate wiki scrapes that confirmed Combat
# Fury's cost as 1/2/3/4 instead of 1/2/4/6, then Combat Stability's rank 3
# as a real 6 - matching what its own guess had already said - and First
# Aid's rank 2 as a real 1, not the 2 its old guess had said.)
#
# The BUILD code below also happens to be a genuine backward-compatibility
# case: it was generated by an older version of the app, before the
# total-points concept was removed, and still carries a "t" (totalPoints)
# field in its decoded payload - loading it here without error, with
# nothing depending on that field anymore, is itself the regression test
# for "an old share link/save must not break just because a field it used
# to carry stopped being read."
