    # Ranks 1-4 are Packrat's own real ranks, and now render as PLAIN
    # numbers - Combat Stability rank 3 (a step earlier in the click order)
    # used to be guessed too, but is now confirmed real at exactly the
    # value its own guess already said, so the real cumulative has caught
    # back up to the blended one by the time Packrat's real ranks are
    # reached (no divergence left to show). Ranks 5-10 are each
    # independently guessed and reintroduce the blend from there - every
    # one of the 10 must still show a total exactly 1 higher than the row
    # before it, proving neither Packrat's real ranks nor its guessed ones
